package com.example.day1;

public class DataTypeExample {
    public static void main(String[] args) {


        byte NumByte = 10;
        short NumShort = 23;
        int NumInt = 12;

        /* l  at the end of the value provided to NumLong variable helps to compiler to identify
        the datatype of the value
         */

        long NumLong = 12346l;

        float Num_float = 4.5f;       //f at the end of the float value is compulsory
        double NumDouble = 55.22;

        boolean b = true;

        char CharStr = 'r';

        System.out.println("Value of byte is : " + NumByte);
        System.out.println("Value of short is : " + NumShort);
        System.out.println("Value of int is : " + NumInt);
        System.out.println("Value of long is : " + NumLong);
        System.out.println("Value of float is : " + Num_float);
        System.out.println("Value of double is : " + NumDouble);
        System.out.println("Value of boolean is : " + b);
        System.out.println("Value of char is : " + CharStr) ;

    }

    }

