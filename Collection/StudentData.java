package com.Collection;

public class StudentData
{
    int sid;
    String sname;
    double marks;

    public void setSname(String sname)
    {
        this.sname = sname;
    }
    public void setSid(int sid)
    {
        this.sid = sid;
    }
    public void setMarks(double marks)
    {
        this.marks = marks;
    }

    public int getSid()
    {
        return sid;
    }
    public String getSname()
    {
        return sname;
    }

    public double getMarks()
    {
        return marks;
    }
}
